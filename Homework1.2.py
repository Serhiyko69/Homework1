x = float(input("Введіть значення x: "))
y = float(input("Введіть значення y: "))

# арифметичні операції
suma = x + y
riznuca = x - y
mnozh = x * y
dil = x / y
dil_cile = x // y

# Вивести результати
print("Сума: ", suma)
print("Різниця: ", riznuca)
print("Добуток: ", mnozh)
print("Ділення: ", dil)
print("Цілочисельне ділення: ", dil_cile)